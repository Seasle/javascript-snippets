class Unit {
    static B(value) {
        return value;
    }

    static KB(value) {
        return value * 1000;
    }

    static KiB(value) {
        return value * 1024;
    }

    static MB(value) {
        return value * 1000 ** 2;
    }

    static MiB(value) {
        return value * 1024 ** 2;
    }

    static GB(value) {
        return value * 1000 ** 3;
    }

    static GiB(value) {
        return value * 1024 ** 3;
    }

    static TB(value) {
        return value * 1000 ** 4;
    }

    static TiB(value) {
        return value * 1024 ** 4;
    }

    static PB(value) {
        return value * 1000 ** 5;
    }

    static PiB(value) {
        return value * 1024 ** 5;
    }

    static EB(value) {
        return value * 1000 ** 6;
    }

    static EiB(value) {
        return value * 1024 ** 6;
    }

    static ZB(value) {
        return value * 1000 ** 7;
    }

    static ZiB(value) {
        return value * 1024 ** 7;
    }

    static YB(value) {
        return value * 1000 ** 8;
    }

    static YiB(value) {
        return value * 1024 ** 8;
    }
}
